package com.jayp;

public class Rooms {

    private double height;
    private double breadth;
    private double length;

    public Rooms(double height, double breadth, double length) {
        this.height = height;
        this.breadth = breadth;
        this.length = length;
    }

    public double getHeight() {
        return height;
    }

    public double getBreadth() {
        return breadth;
    }

    public double getLength() {
        return length;
    }

}